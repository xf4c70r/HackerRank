def migratoryBirds(arr):

    id_ar=list(set(arr))
    id_ar.sort()
    counter=[]
    for i in range(len(id_ar)):
        counter.append(arr.count(id_ar[i]))
    return(id_ar[counter.index(max(counter))])
        

